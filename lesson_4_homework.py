print('TASK2. Введіть свій номер телефону у 10-значному форматі (починаючи з "0"): ')
phone_number = input() #вводимо номер телефону
if len(phone_number) == 10 and phone_number.isdigit() == True: #перевіряємо довжину введеного рядка і тип даних
    print('Дякуємо, номер введено правильно.')
else:
    print('Некоректне введення номеру')

